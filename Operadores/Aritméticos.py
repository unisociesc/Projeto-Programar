#Operadores Aritméticos

num1 = int(45)
num2 = int(2)
 
adiacao = num1 + num2 # Adição
sub = num1 - num2 # Subtração
mult = num1 * num2 # Multiplicação
div = num1 / num2 # Divisão Exata
divinteira = num1 // num2 # Divisão Inteira
mod = num1 % num2  # Módulo (Resto da div)
exp = num1**num2 # Exponenciação

print(adiacao)
print(sub)
print(mult)
print(div)
print(divinteira)
print(mod)
print(exp)

