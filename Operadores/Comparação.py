#Operadores de Comparação

num1 = int(10)
num2 = int(10)

print('10 é menor que 10 ?', num1<num2)
print('Num1 é maior que Num2 ?', num1>num2)
print('Num1 é igual ao Num2 ?', num1==num2)
print('Num1 é diferente do Num2 ?', num1!=num2)
print('Num1 é menor ou igual ao Num2 ?', num1<=num2)
print('Num1 é maior ou igual ao Num2 ?', num1>=num2)

